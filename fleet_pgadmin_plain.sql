BEGIN;

DROP TABLE IF EXISTS part_vehicle_compatibility;
DROP TABLE IF EXISTS telemetry_data;
DROP TABLE IF EXISTS service_records;
DROP TABLE IF EXISTS parts;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS vehicles;

CREATE TABLE vehicles (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vin VARCHAR NOT NULL,
    make VARCHAR NOT NULL,
    model VARCHAR NOT NULL,
    year INTEGER NOT NULL,
    engine_type VARCHAR,
    status VARCHAR(14)
);

CREATE TABLE parts (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    part_number VARCHAR NOT NULL,
    name VARCHAR NOT NULL,
    quantity INTEGER,
    supplier VARCHAR,
    compatible_makes VARCHAR
);

CREATE TABLE telemetry_data (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_id INTEGER NOT NULL,
    timestamp TIMESTAMP,
    speed DOUBLE PRECISION,
    engine_rpm DOUBLE PRECISION,
    coolant_temp DOUBLE PRECISION,
    CONSTRAINT fk_telemetry_vehicle FOREIGN KEY (vehicle_id) REFERENCES vehicles (id)
);

CREATE TABLE service_records (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_id INTEGER NOT NULL,
    date TIMESTAMP,
    description VARCHAR NOT NULL,
    cost DOUBLE PRECISION NOT NULL,
    CONSTRAINT fk_service_vehicle FOREIGN KEY (vehicle_id) REFERENCES vehicles (id)
);

CREATE TABLE users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR NOT NULL,
    hashed_password VARCHAR NOT NULL
);

CREATE TABLE part_vehicle_compatibility (
    part_id INTEGER NOT NULL,
    vehicle_id INTEGER NOT NULL,
    PRIMARY KEY (part_id, vehicle_id),
    CONSTRAINT fk_pvc_part FOREIGN KEY (part_id) REFERENCES parts (id),
    CONSTRAINT fk_pvc_vehicle FOREIGN KEY (vehicle_id) REFERENCES vehicles (id)
);

