SELECT setval(pg_get_serial_sequence('vehicles', 'id'), COALESCE(MAX(id), 1), MAX(id) IS NOT NULL) FROM vehicles;
SELECT setval(pg_get_serial_sequence('parts', 'id'), COALESCE(MAX(id), 1), MAX(id) IS NOT NULL) FROM parts;
SELECT setval(pg_get_serial_sequence('telemetry_data', 'id'), COALESCE(MAX(id), 1), MAX(id) IS NOT NULL) FROM telemetry_data;
SELECT setval(pg_get_serial_sequence('service_records', 'id'), COALESCE(MAX(id), 1), MAX(id) IS NOT NULL) FROM service_records;
SELECT setval(pg_get_serial_sequence('users', 'id'), COALESCE(MAX(id), 1), MAX(id) IS NOT NULL) FROM users;

CREATE INDEX ix_vehicles_make ON vehicles (make);
CREATE INDEX ix_vehicles_id ON vehicles (id);
CREATE UNIQUE INDEX ix_vehicles_vin ON vehicles (vin);
CREATE INDEX ix_vehicles_model ON vehicles (model);
CREATE UNIQUE INDEX ix_parts_part_number ON parts (part_number);
CREATE INDEX ix_parts_id ON parts (id);
CREATE INDEX ix_telemetry_data_timestamp ON telemetry_data (timestamp);
CREATE INDEX ix_telemetry_data_id ON telemetry_data (id);
CREATE INDEX ix_service_records_id ON service_records (id);
CREATE UNIQUE INDEX ix_users_username ON users (username);
CREATE INDEX ix_users_id ON users (id);

COMMIT;
